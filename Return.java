import java.util.Date;

public class Return {
  private int id;
  private User user;
  private Book book;
  private Date returnDate;
  private double fine;
  
  public int getId() {
    return id;
  }
  public User getUser() {
    return user;
  }
  public Book getBook() {
    return book;
  }
  public Date getReturnDate() {
    return returnDate;
  }
  public double getFine() {
    return fine;
  }
}
